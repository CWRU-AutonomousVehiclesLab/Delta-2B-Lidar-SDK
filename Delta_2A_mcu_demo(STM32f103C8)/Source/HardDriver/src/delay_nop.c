//毫秒延时函数
//参数:
//         nus:  延时ms数
//返回值:  无
/***********************************************************************************************************************************/
void delay_ms_nop(uint32_t nms)
{
	uint32_t i;
	for(i=0;i<nms;i++)
	{
		delay_us_nop(1060);
	}
}
